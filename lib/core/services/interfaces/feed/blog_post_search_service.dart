import 'package:flutter/material.dart';

abstract interface class BlogPostSearchService {
  /// Сбрасывает настройки фильтра к значениям по умолчанию.
  ///
  /// Является оберткой над методом [setFilter], вызывающей его
  /// без аргументов для очистки текущих условий выборки.
  ///
  /// Возвращает [true], если операция сброса прошла успешно, иначе [false].
  Future<bool> resetFilter();

  /// Устанавливает параметры фильтрации и отправляет их на сервер.
  ///
  /// [query] - текстовая строка для полнотекстового поиска.
  /// [onlyImportant] - если [true], добавляет условие фильтрации
  ///   только для важных записей (важные события).
  /// [dateRange] - диапазон дат для фильтрации по дате создания.
  ///   Если [null], фильтр по дате не применяется.
  ///
  /// Возвращает:
  /// - [true], если сервер успешно принял и применил фильтр.
  /// - [false], если произошла ошибка запроса или валидации ответа.
  Future<bool> setFilter({
    String query = '',
    bool onlyImportant = false,
    DateTimeRange? dateRange,
  });
}
